     public void intiView() {
         goodBye.setVisible(false);
         queues=new PriorityUtilQueues();
         fcQueues=new ProcessQueues();
         process=new ArrayList<ProcessControlBlock>();
         setVariableNames();
         alogrithm.setText(method.name());
         
         if(method==SchedulingMethods.PRIORITY){
             
             for(int i=0;i<processCount;i++){
                 ProcessControlBlock pcb=new ProcessControlBlock();
                 int j=i+1;
                 pcb.ProcessName=" P"+j;
                 pcb.processId=i+1;
                 int arriv=j+1;
                 pcb.arrivalTime=arriv;
                 pcb.priority=getRandomNumber(2,10);
                 pcb.burstTime=getRandomNumber(1,20);
                 
                 System.out.println("careated arrival time"+pcb.arrivalTime);
                 
                 progressBars[i].setMaximum(pcb.burstTime);
                 progressBars[i].setMinimum(0);
                 
                 
                 process.add(pcb);

             }
             
             
            
             ShowPriority();
              queues.readyQueue.addAll(process);
              NonPreemptivePriorityScheduling scheduler=new NonPreemptivePriorityScheduling();
              scheduler.setData(this);
              NonPreemptivePriorityScheduling.currentProcess= queues.readyQueue.poll();
              NonPreemptivePriorityScheduling.currentProcess.processStatus=ProcessState.RUNNING;
              
             
         }else if (method==SchedulingMethods.FCFS){
             alogrithm.setText(method.name());
             for(int i=0;i<processCount;i++){
                 int j=i+1;
                 Process obj=new Process();
                 obj.pcb.ProcessName="P"+j;
                 obj.pcb.parentProcessId=i;
                 obj.pcb.burstTime=getRandomNumber(1,20);
                 progressBars[i].setMaximum(obj.pcb.burstTime);
                 progressBars[i].setMinimum(0);
                 obj.pcb.arrivalTime=getRandomNumber(1,15);
                 fcfs.add(obj);
                
             }
             FCFS createFCFS=new FCFS();
             fcQueues.readyQueue.addAll(fcfs);
             FCFS.currentProcess=fcQueues.readyQueue.poll();
             FCFS.currentProcess.pcb.setProcessState(ProcessState.RUNNING);
             createFCFS.setMain(this);
         }
         
    }
     
