     public void UpdateUI(){
         
         if(NonPreemptivePriorityScheduling.currentProcess!=null){
         ProcessControlBlock uiProcess=NonPreemptivePriorityScheduling.currentProcess;
         System.out.println("updateUI called");
         cpu.setText(uiProcess.ProcessName);
         int index=process.indexOf(uiProcess);
         waitingTimes[index].setText(""+uiProcess.waitingTime);
         int remainTime=uiProcess.burstTime-uiProcess.executed;
         burstTimes[index].setText(""+remainTime);
         progressBars[index].setValue(uiProcess.executed);
         
         }
         else{
         System.out.println("updateUI currentprocess NULL ");

         }
         StringBuilder builder=new StringBuilder();
         Iterator it=queues.readyQueue.iterator();
         while(it.hasNext()){
             ProcessControlBlock ready=(ProcessControlBlock) it.next();
             builder.append("|"+ready.ProcessName.toString());
         }
         readyQueue.setText(builder.toString());
     }
     

